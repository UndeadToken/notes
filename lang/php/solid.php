
Single Responsibility:
A class should have only one reason to change. A class should have a single
responjsibility and that responsibility should be entirely encapsulated by that
class. "a charge assigned to a unique actor to signify its accountabilities
concerning a unique business task"

Open / Closed principle
"Software entities (classes, modules, functions, etc.) should be open for
extension but closed for modification"

